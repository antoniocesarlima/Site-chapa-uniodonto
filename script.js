function openModal() {
    const modal = document.getElementById('modal-proposal-board01');
    modal.classList.add('open');

    modal.addEventListener('click', (e) => {
        if(e.target.id == 'close01' || e.target.id == 'modal-proposal-board01') {
            modal.classList.remove('open');
        }
    })
}

